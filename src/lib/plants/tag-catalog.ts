// クイックタグ＝非分類の世話/記録/特徴/仕立てラベル（#22 / #169 / #221 / #251）。
//
// 植物の分類（カテゴリ→属→品種）は variety-catalog.ts（#143）へ移し、TagPicker は
// それを動的 import でドリルダウンする（#144）。ここに残すのは taxonomy ではない
// 「水やり」「開花」等の世話/記録に加え、品種と別軸の特徴（斑入り等）/仕立て（水耕等）＝
// 投稿で高頻度に使う小さなクイックセット（#221）。いずれも分類語(#166)でなく特徴・方法。
//
// タグに載せる / 載せない の2原則（#251・kako-jun 実機レビュー）:
//   1. 症状・トラブル・失敗はタグにしない（本文に書く）。タグは共通化して集計・検索する
//      価値のある、前向き/中立の定番だけ。→ 徒長・葉焼け・殺虫・うどんこ病・根腐れ のような
//      症状名はここに入れない（「枯らした」「虫がついた」は一言で書けば足りる）。
//   2. 現実の家・個人・私生活を指すタグ禁止。ハノーバ（架空都市）の世界観の内側だけ。
//      → 「我が家の植物」のような現実の家を持ち込む語は入れない。
//
// 共通化が核心（#169）: 皆が同じ定番タグを使えば集計・検索が効く。特定の病名や、個体ごとの
// 細かな斑の種類のように粒が細かすぎる語は各自バラバラに付いて集計を壊すので畳む
// （斑の細分＝錦/黄斑/白斑/中斑/覆輪 は「斑入り」一本へ・#251）。
//
// 並びの規約（#251 で頻度順→作業順/時系列に変更）:
// - 各行の `tags` は **作業順（世話）/ 時系列（記録）** に並べ、頭から自然に辿れるようにする。
// - propagation（繁殖）系は **発芽 → 発根 → 実生** の自然な順で**隣接**させる（#169）。
//   発芽は記録でなく世話に置く（二重カウント防止）。
// - 分類語（多肉植物 等）は入れない（#166＝分類はタグにしない）。
// - TagPicker は先頭 N 件（INLINE_LIMIT）だけ常時インライン表示し、あふれが十分多いときだけ
//   行末「その他」ポップアップで全件見せる（境界は TagPicker 側の表示ロジックで決める）。
//   ここは「作業順/時系列で並んでいる」ことだけ保証し、`more` 等は持たない。
//
// 値は「タグとして本文に入る文字列」（スペースは入力側で _ に正規化する）。

