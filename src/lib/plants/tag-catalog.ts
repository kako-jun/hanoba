// クイックタグ＝非分類の世話/記録ラベル（#22 / #169）。
//
// 植物の分類（カテゴリ→属→品種）は variety-catalog.ts（#143）へ移し、TagPicker は
// それを動的 import でドリルダウンする（#144）。ここに残すのは taxonomy ではない
// 「水やり」「開花」等＝投稿で高頻度に使う世話/記録の小さなクイックセットだけ。
//
// 共通化が核心（#169）: 皆が同じ定番タグを使えば集計・検索が効く。各行の `tags` は
// **頻度順**（よく使う概念ほど先頭）に並べ、定番を網羅する（捏造でなく一般的な園芸用語）。
// TagPicker は先頭 N 件（INLINE_LIMIT）だけ常時インライン表示し、あふれは行末の
// 「その他」ポップアップで**全件**見せる（幅でクロップして候補を消さない）。
// → 境界（インライン/ポップアップ）はデータでなく TagPicker 側の表示ロジックで決める。
//   ここは「頻度上位が先頭」という並びだけ保証し、`more` 等は持たない。
//
// 並びの規約:
// - propagation（繁殖）系は **発芽 → 発根 → 実生** の自然な順で**隣接**させる（世話の中・#169）。
//   発芽は記録から世話へ移し、記録には置かない（二重カウント防止）。
// - 「発根管理」のような世話「発根」との近接同義は片方（発根）へ寄せて重複させない。
// - 分類語（多肉植物 等）は入れない（#166＝分類はタグにしない）。
//
// 値は「タグとして本文に入る文字列」（スペースは入力側で _ に正規化する）。

export interface TagCategory {
  /** 見出し（カテゴリ名）。 */
  label: string;
  /** そのカテゴリの代表タグ（頻度順・タップで本文へ挿入）。先頭 N をインライン・全件をポップアップに出す。 */
  tags: string[];
}

